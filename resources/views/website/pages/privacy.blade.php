                                        <h6>PRÉAMBULE</h6>

                                        <p>
                                            La fourniture du service par la société CentralResource implique l’existence
                                            d’un ou de plusieurs traitements de données à caractère personnel qui sont
                                            nécessaires à l’exécution du service et basés, sur le consentement des
                                            utilisateurs lors de la validation du service.
                                        </p>

                                        <p>
                                            Dans ce cadre, CentralResource.est responsable du traitement des données à
                                            caractère personnel.
                                        </p>

                                        <p>
                                            En conséquence, la société s’engage (i) à se conformer à la réglementation
                                            en vigueur en matière de protection des données à caractère personnel, et,
                                            en particulier à imposer les mêmes obligations à son personnel et aux tiers
                                            (fournisseurs, filiales, sous-traitants) agissant sous son contrôle dans le
                                            cadre du service.
                                        </p>

                                        <h6>DÉFINITIONS</h6>

                                        <p>
                                            Les termes ci-dessous définis auront entre les parties la signification
                                            suivante :
                                        </p>

                                        <ul>
                                            <li>«données personnelles»: désigne toute information se rapportant à une
                                                personne physique identifiée ou identifiable, directement ou
                                                indirectement, notamment par référence à un identifiant, tel qu'un nom,
                                                un numéro d'identification, des données de localisation, un identifiant
                                                en ligne, ou à un ou plusieurs éléments spécifiques propres à son
                                                identité physique, physiologique, génétique, psychique, économique,
                                                culturelle ou sociale;</li>
                                            <li>«données sensibles»: désigne toutes données à caractère personnel, qui
                                                révèlent l’origine raciale ou ethnique, les opinions politiques, les
                                                convictions religieuses ou philosophiques ou l'appartenance syndicale.
                                            </li>
                                        </ul>

